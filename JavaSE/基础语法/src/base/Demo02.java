package base;

public class Demo02 {
    public static void main(String[] args) {
        //8大基本数据类型

        //整数
        int num1 = 10; //最常用
        byte num2 = 20;
        short num3 = 30;
        long num4 = 30L;  //long类型后面加L


        //小数：浮点数
        float num5 = 50.1F; //float类型后面加F
        double num6 = 3.141592653589793238462643;


        //字符类型
        char name = 'a';

        //字符串 String 是一个类，不是关键字
        //        String name = "名字";

        //boolean值 布尔型
        boolean flag = true;
        //        boolean代表是非


    }
}
